          佛家在称呼上有一些特殊的名词，有特定的规矩。比如一寺的主持叫“方丈”也叫“住持”。住持是“住于世而保持法统传承”的意思。一般人容易把“住持”写成“主持”，
          这不仅是文字错误，也是理解上的错误。“方丈”的原意，即一丈见方之室，又作方丈室、丈室。禅寺中住持的居室或客殿，也叫函丈、正堂、堂头。印度的僧房多以方一丈为制，进而用来指住持
          的居室，后来又转为对住持的尊称。
        </p>
        
