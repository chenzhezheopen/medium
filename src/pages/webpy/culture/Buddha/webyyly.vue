          并不属于禅宗。后来，朝廷也曾用“禅师”号赐予有名望的僧人，以唐代神秀获得“大通禅师”为最早，其后，禅宗六祖慧能于示寂余百年后，也获得 “大鉴禅师”称号。至于生前及受赐号的僧人，以宋代宗
          杲受赐“大慧禅师”最为著名。
