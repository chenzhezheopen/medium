          身心无卷、成就忍力十种德性。法师的种类，依据《法华经 · 法师品》的记载，可分为受持、读经、诵经、解说、书写五种，称为五种法师。现在法师已经成为汉地出家人的通称，遇见出家人，称其为某某法师，
          是完全可以的，它成为世俗人称呼出家人最常用的敬词。
        </p>
        <p class="m-p">
          “三藏法师”由称为三藏比丘、三藏圣师，或略称三藏，是对精通佛教圣典中之经、律、论三藏者的尊称。在中国，对从事佛经汉译的僧侣，常尊称为译经三藏或三藏法师，如鸠摩罗什、真谛、玄奘等人即有此称。
          小说《西游记》中，称玄奘为“唐三藏”、“唐僧”，三藏好像称谓玄奘的专有名词，这虽然没有什么大错，但 “三藏” 的名称其他人也可以用，并非专有名词。
        </p>
